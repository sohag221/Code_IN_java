public class Sequence_URI1098 {
    public static void main(String[] args) {
        for (int i=1;i<=3;i++){
            System.out.print("I=0"+" ");
            System.out.println("J="+i);
        }
        for (int j=1;j<=3;j++){
            System.out.print("I=0.2 ");
            System.out.println("J="+(j+0.2));
        }
        System.out.println(".....");
        for (int m=1;m<=3;m++){
            System.out.println("I=2"+" "+"J=?");
        }
    }
}
